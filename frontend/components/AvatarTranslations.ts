export const getAvatarTerm = (key: string, lang: string = 'en'): string => {
    // Handle skin/hair colors (often Capitalized in API but keys might be mixed)
    // We check direct key first
    let tr = AVATAR_TR[key];
    
    // Check capitalized/unicapitalized variants
    if (!tr) {
        // Try uppercase first char
        const capKey = key.charAt(0).toUpperCase() + key.slice(1);
        tr = AVATAR_TR[capKey];
    }
    
    // For Eye/Mouth types which are just emotions, we can format them or translate if needed.
    // Currently fallback to formatted text.

    if (tr && tr[lang]) return tr[lang];
    if (tr && tr['en']) return tr['en']; // Fallback to EN translation
    if (tr && tr[lang.split('-')[0]]) return tr[lang.split('-')[0]]; // Handle 'zh-CN' -> 'zh'

    // Formatter fallback: 'shortWaved' -> 'Short Waved'
    return key.replace(/([A-Z])/g, ' $1').trim();
};
